using System;
using System.Collections.Generic;
using System.Linq;

namespace SystemExtend.Drawing
{
	public class Color
	{
		public Color()
		{
		}

		private int r;

		public int R
		{
			get { return r; }
			set { r = value; }
		}

		private int g;

		public int G
		{
			get { return g; }
			set { g = value; }
		}

		private int b;

		public int B
		{
			get { return b; }
			set { b = value; }
		}
	}
}
